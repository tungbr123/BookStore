    @Transactional
    @Override
    public ApiResponse<Object> confirmResetPassword(String token, String newPassword) {
        ConfirmationToken confirmationToken = confirmationTokenService
                .getToken(token)
                .orElseThrow(() ->
                        new IllegalStateException("Token not found"));

        if (confirmationToken.getConfirmedAt() != null) {
            throw new IllegalStateException("Your password already change");
        }

        LocalDateTime expiredAt = confirmationToken.getExpiresAt();

        if (expiredAt.isBefore(LocalDateTime.now())) {
            throw new IllegalStateException("Token expired, plz send again require");
        }
        try {
            confirmationTokenService.setConfirmedAt(token);
            var user = confirmationTokenService.getUser(token);
            user.setPassword(passwordEncoder.encode(newPassword));
            return ApiResponse.builder()
                    .statusCode("200")
                    .message("Change password successfully")
                    .build();
        } catch (Exception e) {
            return ApiResponse.builder()
                    .statusCode("500")
                    .message("Change password failed, " + e)
                    .build();
        }

    }
