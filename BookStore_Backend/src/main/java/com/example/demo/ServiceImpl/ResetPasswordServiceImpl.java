        _User user = userService.findByEmail(email);
        if(user != null) {
            ConfirmationToken confirmationToken = new ConfirmationToken(
                    token,
                    LocalDateTime.now(),
                    LocalDateTime.now().plusMinutes(15),
                    user
            );
            try {
                confirmationTokenService.saveConfirmationToken(
                        confirmationToken);
                emailService.send(email,buildEmail(email, token));
                return ApiResponse.builder()
                        .statusCode("200")
                        .message("Email sent")
                        .build();
            } catch (Exception e) {
                return ApiResponse.builder()
                        .statusCode("500")
                        .message("Send email failed" + e)
                        .build();
            }
        }
        return ApiResponse.builder()
                .statusCode("500")
                .message("Send email failed")
                .build();
